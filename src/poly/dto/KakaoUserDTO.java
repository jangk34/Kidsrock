package poly.dto;



public class KakaoUserDTO {

	private String kakao_id;

	private String user_seq;

	private String profile_image;

	private String thumbnail_image;

	private String reg_user_seq;

	private String reg_date;

	private String update_user_seq;

	private String update_date;

	

	public String getKakao_id() {

		return kakao_id;

	}

	public void setKakao_id(String kakao_id) {

		this.kakao_id = kakao_id;

	}

	public String getUser_seq() {

		return user_seq;

	}

	public void setUser_seq(String user_seq) {

		this.user_seq = user_seq;

	}

	public String getProfile_image() {

		return profile_image;

	}

	public void setProfile_image(String profile_image) {

		this.profile_image = profile_image;

	}

	public String getThumbnail_image() {

		return thumbnail_image;

	}

	public void setThumbnail_image(String thumbnail_image) {

		this.thumbnail_image = thumbnail_image;

	}

	public String getReg_user_seq() {

		return reg_user_seq;

	}

	public void setReg_user_seq(String reg_user_seq) {

		this.reg_user_seq = reg_user_seq;

	}

	public String getReg_date() {

		return reg_date;

	}

	public void setReg_date(String reg_date) {

		this.reg_date = reg_date;

	}

	public String getUpdate_user_seq() {

		return update_user_seq;

	}

	public void setUpdate_user_seq(String update_user_seq) {

		this.update_user_seq = update_user_seq;

	}

	public String getUpdate_date() {

		return update_date;

	}

	public void setUpdate_date(String update_date) {

		this.update_date = update_date;

	}

}